namespace SkyRez.Common.API;

/// <summary>Абстрактный базовый класс для всех модов SkyRez.<br/>Определяет основной жизненный цикл и свойства мода.<br/>Любой класс мода должен быть унаследован от этого класса.</summary>
public abstract class SkyRezMod
{
    /// <summary>Уникальное имя мода. Например, "SkyRez.Translator".<br/>Используется для идентификации в логах и Harmony.</summary>
    public abstract string Name { get; }

    /// <summary>Версия мода. Например, "1.0.0".<br/>Полезна для отладки и информирования пользователя.</summary>
    public abstract Version Version { get; }

    /// <summary>Этот метод вызывается один раз при загрузке мода.<br/>Здесь должна быть основная логика инициализации (например, применение патчей Harmony).</summary>
    public abstract void OnLoad();

    /// <summary>(Необязательный) Этот метод вызывается при выгрузке мода.<br/>В текущей реализации не используется, но зарезервирован для будущего.</summary>
    public virtual void OnUnload()
    {
        // По умолчанию ничего не делает.
        // Можно переопределить в дочернем классе для отмены патчей.
    }
}